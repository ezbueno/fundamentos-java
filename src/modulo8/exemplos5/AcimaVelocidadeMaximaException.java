package modulo8.exemplos5;

@SuppressWarnings("serial")
public class AcimaVelocidadeMaximaException extends Exception {

	public AcimaVelocidadeMaximaException() {
	}

	public AcimaVelocidadeMaximaException(String message) {
		super(message);
	}

	public AcimaVelocidadeMaximaException(Throwable cause) {
		super(cause);
	}

	public AcimaVelocidadeMaximaException(String message, Throwable cause) {
		super(message, cause);
	}

	public AcimaVelocidadeMaximaException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
