package modulo3;

public class Exercicio2_4 {

	public static void main(String[] args) {
		
		/*
		Imprima a tabuada do 9 (até o décimo valor)
		*/
		
		int mult = 0;
		
		for (int i = 0; i < 10; i++) {
			mult = 9 * (i + 1);
			System.out.println("9 x " + (i + 1) + " = " + mult);
		}
		
	}
}
